//! 3D tags: the `3d_*` and `obj_model_*` family for the three.js layer.
//!
//! Model/texture/sound storages in this family load from `data/others/3d/...`
//! (models, sprites, textures, audio) — not one of the standard asset dirs —
//! so those params stay [`ValueKind::Text`] to avoid false missing-asset
//! warnings. `pos`/`rot`/`scale` accept either a single number or a
//! comma-separated `x,y,z` triple (engine's `$.three_pos`), so they stay
//! [`ValueKind::Text`] even where the engine default is a single number.

use super::{ExtraParams::{Allow, Deny}, TagSpec, opt, optd, req, tag};
